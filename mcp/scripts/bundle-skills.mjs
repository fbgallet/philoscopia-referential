// build step: derive the MCP session-prompts' content from the canonical
// skills/*/SKILL.md files, so the prompts and the skills can never drift.
// Emits src/skills.generated.ts (committed, refreshed on every `npm run build`).
// The skills stay the single source of truth for the session methodology; the
// prompts are just a delivery channel that reuses their body verbatim.

import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const pkgRoot = fileURLToPath(new URL("..", import.meta.url));
const skillsDir = join(pkgRoot, "..", "skills");
const outFile = join(pkgRoot, "src", "skills.generated.ts");

/** Split a `---\n…\n---\n` frontmatter block from the markdown body. */
function parse(src) {
  const m = /^---\n([\s\S]*?)\n---\n?([\s\S]*)$/.exec(src);
  if (!m) throw new Error("missing or malformed frontmatter");
  const meta = {};
  for (const line of m[1].split("\n")) {
    const i = line.indexOf(":");
    if (i === -1) continue;
    meta[line.slice(0, i).trim()] = line.slice(i + 1).trim();
  }
  return { meta, body: m[2].trim() };
}

const skills = [];
if (existsSync(skillsDir)) {
  for (const entry of readdirSync(skillsDir)) {
    if (!entry.startsWith("philo-")) continue;
    const file = join(skillsDir, entry, "SKILL.md");
    if (!existsSync(file)) continue;
    const { meta, body } = parse(readFileSync(file, "utf8"));
    if (!meta.name) throw new Error(`${entry}/SKILL.md: frontmatter has no name`);
    skills.push({ name: meta.name, description: meta.description ?? "", body });
  }
  skills.sort((a, b) => a.name.localeCompare(b.name));
} else {
  console.warn(`bundle-skills: no skills/ dir at ${skillsDir}; emitting an empty set.`);
}

const out =
  "// AUTO-GENERATED by scripts/bundle-skills.mjs — DO NOT EDIT.\n" +
  "// Source of truth: skills/*/SKILL.md. Regenerated on every `npm run build`.\n\n" +
  "export interface SkillProtocol {\n" +
  '  /** The skill name, e.g. "philo-discover". */\n' +
  "  name: string;\n" +
  "  /** The skill's frontmatter description. */\n" +
  "  description: string;\n" +
  "  /** The skill body (markdown, frontmatter stripped): the full session protocol. */\n" +
  "  body: string;\n" +
  "}\n\n" +
  `export const SKILL_PROTOCOLS: SkillProtocol[] = ${JSON.stringify(skills, null, 2)};\n`;

writeFileSync(outFile, out);
console.log(`skills bundled: ${skills.length} protocol(s) → src/skills.generated.ts`);
